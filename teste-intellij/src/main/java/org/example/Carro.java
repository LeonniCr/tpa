package org.example;

public class Carro {
    public String marca;
    public String modelo;
    public String cor;
    public String placa;
    public String nome;
    public Boolean tanqueCheio;

    public Carro(String marca, String modelo, String cor, String placa, String nome, Boolean tanqueCheio) {
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.placa = placa;
        this.nome = nome;
        this.tanqueCheio = tanqueCheio;
    }


}
